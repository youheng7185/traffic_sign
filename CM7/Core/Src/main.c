  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  // init display
  ST7789_Init();
  // reset ov7670
  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_SET);
  HAL_Delay(20);
  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, GPIO_PIN_RESET);
  HAL_Delay(20);
  // init camera
  ov7670_init(&hdcmi, &hdma_dcmi, &hi2c1);

